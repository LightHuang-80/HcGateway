/* USER CODE BEGIN Header_ROSClientMain */
/**
* @brief Function implementing the ROSTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_ROSClientMain */
void ROSClientMain(void *argument)
{
  /* USER CODE BEGIN ROSClientMain */
  char nodeActualValues[256];

  ros::Subscriber<std_msgs::String> subscriber("MTMsg", MsgSubscriberCallback);

  std_msgs::String pubMsg;
  ros::Publisher publisher("MTNodeAct", &pubMsg);

  ROSNodeH->initNode();
  ROSNodeH->subscribe(subscriber);
  ROSNodeH->advertise(publisher);

  uint32_t ticks = 0;

  /* Infinite loop */
  for(;;)
  {
    ROSNodeH->spinOnce();

    if (++ticks >= NODEACTUALVALUES_PUB_DUR){
      ticks = 0;
      MCM_updateNodeActualValues(nodeActualValues, 256);
      pubMsg.data = nodeActualValues;
      publisher.publish(&pubMsg);
    }

    osDelay(1);
  }
  /* USER CODE END ROSClientMain */
}

